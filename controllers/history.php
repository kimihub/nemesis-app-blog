<h1>History</h1>

<p>Source : <a href="https://en.wikipedia.org/wiki/History_of_the_Internet" target="_blank">Wikipedia</a></p>

<h3>Creating the Internet...</h3>
<ul>
  <li><strong>1981 – Computer Science Network (CSNET)</strong></li>
  <li>1982 – TCP/IP protocol suite formalized</li>
  <li>1982 – Simple Mail Transfer Protocol (SMTP)</li>
  <li>1983 – Domain Name System (DNS)</li>
  <li>1985 – First .COM domain name registered</li>
  <li>1988 – OSI Reference Model released, Internet Relay Chat (IRC)</li>
  <li>1990 – Hypertext Transfer Protocol (HTTP)</li>
  <li>IMDb (movie database)</li>
  <li><strong>1991 – World Wide Web (WWW)</strong></li>
  <li>1992 – HyperText Markup Language (HTML)</li>
  <li>Online diaries (Weblog)</li>
  <li>1994 – HTML2, HTTPS, URL/URI, W3C</li>
  <li>Netscape Navigator, Amazon</li>
  <li>1995 – HTML3, Javascript, PHP, JAVA</li>
  <li>Lycos, Yahoo, WikiWikiWeb, Microsoft Internet Explorer</li>
  <li>1996 – HTML4, ECMAScript, Shockwave Flash, XML, CSS</li>
  <li>Internet Archive, Hotmail, Caramail, Go Daddy</li>
  <li>1998 – Wi-Fi, P2P, CSS2, BBCode, RSS, JSON</li>
  <li>Mozilla, Google, PayPal, Napster, BitTorrent, Wikipedia, SourceForge</li>
  <li>MSN Messenger, Blogger, phpBB, Meetup, DeviantArt</li>
  <li><strong>2003 – Web 2.0, Ajax</strong></li>
  <li>Second Life, Steam, WoW</li>
  <li>Linkedin, Myspace, Flickr, CouchSurfing, Gmail, iTunes</li>
  <li>Facebook, YouTube, Megaupload, Deezer, Twitter, Tumblr</li>
  <li><strong>2008 – HTML5, Cloud computing, RESTful Web Services, V8 Google Chrome</strong></li>
  <li>Dropbox, GitHub, Stack Overflow</li>
  <li>2009 – Bitcoin, H.264/AVC, Node.js, golang</li>
  <li>Kickstarter, Instagram, Vine</li>
  <li>2011 – WebRTC, WebSockets</li>
  <li>2013 – Firefox OS, Ubuntu Touch</li>
  <li><strong>2015 – Web 3.0</strong></li>
  <li>Ethereum</li>
</ul>
